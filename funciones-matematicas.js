export function sumar(n1,n2) {
    return n1 + n2
}
export function restar(n1,n2) {
    return n1 - n2
}
export function multiplicar(n1,n2) {
    return n1 * n2
}
export function dividir(n1,n2) {
    return n1 / n2
}

